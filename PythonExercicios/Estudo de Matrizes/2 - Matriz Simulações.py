#Elevando números da lista e mantendo dentro do formato de matriz

numeros = [1,2,3,4,5]
num_elevados = [numeros ** 2 for numeros in numeros]
print(num_elevados)

#Multiplicando números e mantendo dentro de formato matriz

numeros = [1,2,3,4,5]
num_multi = [numeros * 2 for numeros in numeros]
print(num_multi)

#dividindo números e mantendo dentro de formato matriz

numeros = [1,2,3,4,5]
num_Div = [numeros / 2 for numeros in numeros]
print(num_Div)
